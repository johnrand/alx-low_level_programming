#ifndef _3_FUNCTION_LIKE_MACRO_H_
#define _3_FUNCTION_LIKE_MACRO_H_

#define ABS(x) ((x) < 0 ? -(x) : (x))

/**
 * ABS - Macro that computes the absolute value of a number x
 * @x: The input number
 *
 * Description: This macro takes a number x and computes its absolute value.
 *              It uses a ternary operator to determine if x is negative,
 *              in which case it returns the negation of x, otherwise it
 *              returns x itself.
 *
 * Return: The absolute value of x
 */

#endif /* _3_FUNCTION_LIKE_MACRO_H_ */
